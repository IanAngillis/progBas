package opdrachten;

public class Opdracht12 {

	public static void main(String[] args) {
		byte x = 2;
		
		switch(x) {
			case 1: System.out.println("Het getal is 1");
					break;
			case 2: System.out.println("Het getal is 2");
					break;
			case 3: System.out.println("Het getal is 3");
					break;
			default:
				System.out.println("het getal is niet 1, 2 of 3");
		
		}
	}

}
